package Core_Java.Collection.List.ArrayList;

public class Student1 {
	int rollNum;
	String name;
	double per;
	
	public Student1(int rollNum, String name,double per)
	{
		this.name=name;
		this.rollNum=rollNum;
		this.per=per;
	}

	public int getRollNum() {
		return rollNum;
	}

	public String getName() {
		return name;
	}

	public double getPer() {
		return per;
	}
	}

