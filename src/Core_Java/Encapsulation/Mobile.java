package Core_Java.Encapsulation;

public class Mobile {
	private String name = "Galaxy";
	private int price = 10000;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
	

}
